// --- Arithmetic Operators ---
let a = 10;
let b = 3;

console.log("---- Arithmetic Operators ----");
console.log("a + b =", a + b);   // 13
console.log("a - b =", a - b);   // 7
console.log("a * b =", a * b);   // 30
console.log("a / b =", a / b);   // 3.333...
console.log("a % b =", a % b);   // 1 (remainder)
console.log("a ** b =", a ** b); // 1000 (10^3)

// --- Comparison Operators ---
let x = 5;
let y = "5";

console.log("\n---- Comparison Operators ----");
console.log("x == y :", x == y);     // true (checks value only)
console.log("x === y :", x === y);   // false (checks value and type)
console.log("x != y :", x != y);     // false
console.log("x !== y :", x !== y);   // true

let m = 7;
let n = 10;

console.log("m > n :", m > n);       // false
console.log("m < n :", m < n);       // true
console.log("m >= 7 :", m >= 7);     // true
console.log("n <= 5 :", n <= 5);     // false
